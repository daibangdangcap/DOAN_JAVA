    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int sl=Integer.parseInt(tfshowSoLuong.getText().toString());
        if(sl>=soluong)
        {
            JOptionPane.showMessageDialog(rootPane, "Số lượng phải bé hơn số lượng tồn của thiết bị");
        }
        else
        {
            sl++;
            tfshowSoLuong.setText(String.valueOf(sl));
            double thanhtien=giatien*sl;
            tfshowThanhTien.setText(String.valueOf(thanhtien));
        }
    }//GEN-LAST:event_btnAddActionPerformed

