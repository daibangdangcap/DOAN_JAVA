    private void btnDeleteTBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteTBActionPerformed
        // TODO add your handling code here:
        if(tfDeleteTB.getText().contentEquals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Số thứ tự thiết bị không hợp lệ");
        }
        else
        {
            int stt=Integer.parseInt(tfDeleteTB.getText());
            DefaultTableModel model=(DefaultTableModel)tbListThietBi.getModel();
            int sttEnd=model.getRowCount();
            if(stt>sttEnd||stt<1)
            {
                JOptionPane.showMessageDialog(rootPane, "Số thứ tự không hợp lệ");
            }
            else{
                model.removeRow(stt-1);
                for(int i=0;i<sttEnd-1;i++)
                {
                    model.setValueAt(i+1, i, 0);
                }
            }
        }
    }//GEN-LAST:event_btnDeleteTBActionPerformed

    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        // TODO add your handling code here:
        int tongsl=0;
        double tongtien=0;
        DefaultTableModel model=(DefaultTableModel)tbListThietBi.getModel();
        for(int i=0;i<model.getRowCount();i++)
        {
            tongsl=tongsl+Integer.parseInt(model.getValueAt(i, 2).toString());
            tongtien=tongtien+Double.parseDouble(model.getValueAt(i, 3).toString());
        }
        String idHD="HD"+new generateRandomString().generateRandomString(2);
        MyConnection con=new MyConnection();
        PreparedStatement ps;
        String addHD="insert into ltjava_doan.hoadon values (?,NULL,?,?,?)";
        try
        {
            ps=con.getConnection().prepareStatement(addHD);
            ps.setString(1, idHD);
            ps.setDate(2, new java.sql.Date(new Date().getTime()));
            ps.setInt(3,tongsl);
            ps.setDouble(4, tongtien);
            ps.executeUpdate();
            addToCTHD(idHD);
            removeAllRow();
            JOptionPane.showMessageDialog(rootPane, "Thêm hóa đơn thành công");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "Error"+e);
        }
        
    }//GEN-LAST:event_btnThanhToanActionPerformed

