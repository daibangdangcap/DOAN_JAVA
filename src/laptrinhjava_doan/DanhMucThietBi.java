    private void init(){
        ArrayList<CardView> lstCardView=new ArrayList<>();
        MyConnection con=new MyConnection();
        PreparedStatement ps;
        ResultSet rs;
        String query="select * from ltjava_doan.thietbi";
        try
        {
            ps=con.getConnection().prepareStatement(query);
            rs=ps.executeQuery();
            while(rs.next())
            {
                Model_card cv=new Model_card();
                cv.setId(rs.getString("IDThietBi"));
                cv.setTitle(rs.getString("TenThietBi"));
                cv.setDescription(String.valueOf(rs.getDouble("DonGia")));
                cv.setSoluong(rs.getInt("SoLuong"));
                byte[]imageData=rs.getBytes("imageTB");
                if(imageData!=null)
                {
                    InputStream in=new ByteArrayInputStream(imageData);
                    BufferedImage image=ImageIO.read(in);
                    cv.setIcon(new ImageIcon(image));
                }
                lstCardView.add(new CardView(cv));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(jPanel2, "Error"+e);
        }
        jPanel2.setLayout(new WrapLayout(WrapLayout.LEADING));
        jScrollPane1.setVerticalScrollBar(new JScrollBar());
        for(CardView item : lstCardView)
        {
            jPanel2.add(item);
            item.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                IDThietBi=item.db.getId();
                String title=item.db.getTitle();
                String dongia=item.db.getDescription();
                giatien=Double.parseDouble(dongia);
                soluong=item.db.getSoluong();
                tfshowTenThietBi.setText(title);
                tfshowSoLuong.setText("1");
                tfshowThanhTien.setText(dongia);
            }
            });
        }
        jPanel2.revalidate();
        jPanel2.repaint();
    }
